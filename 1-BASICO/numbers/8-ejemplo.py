import math

# Definición de números
a = 10
b = 3.5
c = 1 + 2j

# Operaciones básicas
suma = a + b
multiplicacion = a * b
division = a / b
exponente = a ** 2

# Funciones matemáticas
raiz_cuadrada = math.sqrt(a)
valor_absoluto = abs(-a)

# Números complejos
d = c + (2 + 3j)
division_compleja = c / (1 + 1j)

# Resultados
print(f"Suma: {suma}")
print(f"Multiplicación: {multiplicacion}")
print(f"División: {division}")
print(f"Exponente: {exponente}")
print(f"Raíz cuadrada: {raiz_cuadrada}")
print(f"Valor absoluto: {valor_absoluto}")
print(f"Número complejo: {d}")
print(f"División compleja: {division_compleja}")
