# Solicita al usuario ingresar su nombre
name = input("Ingrese su nombre: ")

# Muestra el nombre ingresado por el usuario
print(name)

# Muestra el tipo de dato de la variable 'name'
print(type(name))  # Resultado: <class 'str'>

# Solicita al usuario ingresar su edad y convierte el valor a entero
age = int(input("Ingrese su edad: "))

# Muestra el tipo de dato de la variable 'age'
print(type(age))  # Resultado: <class 'int'>
